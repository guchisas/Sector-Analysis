def get_stock_list():
    """重複を除いた銘柄リストを返す"""
    seen = set()
    unique_list = []
    for ticker, name, sector in STOCK_LIST:
        if ticker not in seen:
            seen.add(ticker)
            unique_list.append((ticker, name, sector))
    return unique_list


def get_tickers():
    """ティッカーシンボルのリストのみを返す"""
    return [t for t, _, _ in get_stock_list()]


def get_sector_map():
    """ティッカー→セクターのマッピング辞書を返す"""
    return {t: s for t, _, s in get_stock_list()}


def get_name_map():
    """ティッカー→銘柄名のマッピング辞書を返す"""
    return {t: n for t, n, _ in get_stock_list()}
